package Model;

public class Time {
	private int year;
	private int month;
	private int date;
	private int hour;
	private int minute;
	private int second;
	
	public Time(int y, int m, int d, int h, int min, int s) {
		year = y;
		month = m;
		date = d;
		hour = h;
		min = m;
		second = s;
		
	}
	
}
